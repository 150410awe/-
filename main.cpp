data_management management;

int32_t main() {


	string choice;

	management.start_menu();
	while (cin >> choice) {
		if (choice == "1")
			management.login(management.students);
		if (choice == "2")
			management.login(management.teachers);
		if (choice == "3")
			management.login(management.admins);
		if (choice == "4")
			management.add_administrator();
		if (choice == "0") {
			management.exit_save();
			break;
		}
		else 
			cout << "选择错误" << endl;
		management.start_menu();
	}


	return 0;
}